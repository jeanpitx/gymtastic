class SendContactRecibidaClient extends Notification
{
    use Queueable;

    /**
     * Create a new notification instance.
     *
     * @return void
     */
    public function __construct()
    {
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return \Illuminate\Notifications\Messages\MailMessage
     */
    public function toMail($notifiable)
    {
        $subject = sprintf('%s: Solicitud de productos BCM!', config('app.name'));
        $greeting = sprintf('Hola %s!', $notifiable->routes['name']);


        return (new MailMessage)
                        ->subject($subject)
                        ->greeting($greeting)
                        ->salutation('Atentamente '.config('app.name'))
                        ->line($notifiable->routes['mensaje'])
                        ->line('Para mas información acerca de todos nuestros servicios visita nuestro sitio web a continuación:')
                        ->action("Sitio Web", url('/'))
                        ->line('Gracias por utilizar nuestros servicios.!');
    }

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            //
        ];
    }
}
